const worksRoot = document.querySelector("#works");

const fragment = document.createDocumentFragment();

works.forEach((work, index) => {
  const article = document.createElement("article");
  article.className = work.images ? "work work-group" : "work";

  const optionsText = work.titleOptions.map((option, optionIndex) => `${optionIndex + 1}. ${option}`).join("\n");
  const textForCopy = `${work.parsha} - ${work.title}\n${work.verse}\n\n${work.text}\n\nTitle options:\n${optionsText}`;
  const imageMarkup = work.images
    ? `<div class="image-grid">${work.images.map((image, imageIndex) => `<img src="${image}" alt="${work.title} ${imageIndex + 1}" loading="lazy">`).join("")}</div>`
    : `<img src="${work.image}" alt="${work.title}" loading="${index < 2 ? "eager" : "lazy"}">`;

  article.innerHTML = `
    <div class="image-wrap">
      ${imageMarkup}
    </div>
    <section class="text-panel">
      <div class="meta">
        <span class="count">${String(index + 1).padStart(2, "0")} / ${works.length}</span>
        <button class="copy-button" type="button" data-copy="${escapeHtml(textForCopy)}">העתק טקסט</button>
      </div>
      <h2 class="work-title">${work.title}</h2>
      <p class="parsha">${work.parsha}</p>
      <p class="verse">${work.verse}</p>
      <p class="work-text">${work.text}</p>
      <div class="title-options" dir="ltr">
        <p class="title-options-label">Title options</p>
        <ol>
          ${work.titleOptions.map(option => `<li>${option}</li>`).join("")}
        </ol>
      </div>
    </section>
  `;

  fragment.append(article);
});

worksRoot.append(fragment);

document.addEventListener("click", async (event) => {
  const button = event.target.closest(".copy-button");
  if (!button) return;

  const text = button.dataset.copy;
  const original = "העתק טקסט";
  button.textContent = "הועתק";
  button.classList.add("copied");

  try {
    await copyText(text);
    window.setTimeout(() => {
      button.textContent = original;
      button.classList.remove("copied");
    }, 1800);
  } catch {
    button.textContent = "לא הועתק";
    window.setTimeout(() => {
      button.textContent = original;
      button.classList.remove("copied");
    }, 1800);
  }
});

async function copyText(text) {
  if (navigator.clipboard && window.isSecureContext) {
    await navigator.clipboard.writeText(text);
    return;
  }

  const textarea = document.createElement("textarea");
  textarea.value = text;
  textarea.setAttribute("readonly", "");
  textarea.style.position = "fixed";
  textarea.style.inset = "0 auto auto 0";
  textarea.style.opacity = "0";
  document.body.append(textarea);
  textarea.select();
  document.execCommand("copy");
  textarea.remove();
}

function escapeHtml(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll('"', "&quot;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}
